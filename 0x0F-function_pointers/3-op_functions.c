#include "3-calc.h"
/**
 * op_add - Returns the addition of two argument
 * @a: The first argument
 * @b: The second argument
 * Return: The sum the two
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - Return the difference
 * @a: The first argument
 * @b: The second argument
 * Return: The difference of the two
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - Return The product
 * @a: The first argument
 * @b: The second argument
 * Return: The product of the two
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - Return the division 
 * @a: The first argument
 * @b: The second argument
 * Return: The qoutient of the two
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - Return the remaining
 * @a: The first argument
 * @b: The second argument
 * Return: The remainder of the two
 */
int op_mod(int a, int b)
{
return (a % b);
}
